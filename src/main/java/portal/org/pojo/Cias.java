package portal.org.pojo;

public class Cias {
	private String idcia;
	private String namecia;
	private boolean activo;
	private String dir1;
	private String tel;
	
	public Cias() {
	
	}

	public String getIdcia() {
		return idcia;
	}

	public void setIdcia(String idcia) {
		this.idcia = idcia;
	}

	public String getNamecia() {
		return namecia;
	}

	public void setNamecia(String namecia) {
		this.namecia = namecia;
	}

	public boolean isActivo() {
		return activo;
	}

	public void setActivo(boolean activo) {
		this.activo = activo;
	}

	public String getDir1() {
		return dir1;
	}

	public void setDir1(String dir1) {
		this.dir1 = dir1;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}
	

}
